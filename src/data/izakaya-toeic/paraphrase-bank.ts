export function getParaphrasesByCategory(category: ParaphraseCategory): ParaphraseEntry[] {
  return PARAPHRASE_BANK.filter(entry => entry.category === category);
}

export function getParaphrasesByDifficulty(difficulty: 'basic' | 'intermediate' | 'advanced'): ParaphraseEntry[] {
  return PARAPHRASE_BANK.filter(entry => entry.difficulty === difficulty);
}

export function getParaphrasesByPart(part: number): ParaphraseEntry[] {
  return PARAPHRASE_BANK.filter(entry => entry.partRelevance.includes(part));
}

export function getRandomParaphraseQuiz(count: number): ParaphraseEntry[] {
  const shuffled = [...PARAPHRASE_BANK].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, Math.min(count, PARAPHRASE_BANK.length));
}

export function getRandomParaphraseQuizByCategory(
  category: ParaphraseCategory,
  count: number
): ParaphraseEntry[] {
  const filtered = getParaphrasesByCategory(category);
  const shuffled = [...filtered].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, Math.min(count, filtered.length));
}

export function getParaphraseBankStats() {
  const total = PARAPHRASE_BANK.length;
  const byCategory: Record<string, number> = {};
  const byDifficulty: Record<string, number> = {};

  for (const entry of PARAPHRASE_BANK) {
    byCategory[entry.category] = (byCategory[entry.category] || 0) + 1;
    byDifficulty[entry.difficulty] = (byDifficulty[entry.difficulty] || 0) + 1;
  }

  return { total, byCategory, byDifficulty };
}
