// 居酒屋TOEIC -- Paraphrase Bank
// THE most important TOEIC skill: recognizing that the answer NEVER uses the same words as the audio.

export interface ParaphraseEntry {
  id: string;
  audio: string;        // What you HEAR in the audio
  answer: string;       // How it appears in the correct answer
  category: ParaphraseCategory;
  partRelevance: number[];  // Which TOEIC parts [2,3,4]
  difficulty: 'basic' | 'intermediate' | 'advanced';
  example?: string;     // Full sentence context
  tip?: string;         // Japanese study tip
}

export type ParaphraseCategory =
  | 'verb-swap'           // postpone → put off, purchase → buy
  | 'noun-swap'           // automobile → car, position → job
  | 'expression-swap'     // be in charge of → manage, look into → investigate
  | 'structure-change'    // "She manages" → "She is responsible for"
  | 'negative-positive'   // "not cheap" → "expensive"
  | 'cause-effect'        // "rain caused delay" → "delayed due to weather"
  | 'specific-general'    // "Monday at 3 PM" → "early next week"
  | 'active-passive'      // "John fixed it" → "It was repaired"
  ;

export interface ParaphraseCategory_Meta {
  id: ParaphraseCategory;
  label: string;
  labelJa: string;
  description: string;
  frequency: 'very-high' | 'high' | 'medium' | 'low';
}

