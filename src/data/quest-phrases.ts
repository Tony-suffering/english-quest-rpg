// ── Helper Functions ──────────────────────────────────────

/** Get all phrases for a given stage number */
export function getStage(stage: number): QuestPhrase[] {
  return QUEST_PHRASES.filter((p) => p.stage === stage);
}

/** Get a single phrase by its ID */
export function getPhrase(id: string): QuestPhrase | undefined {
  return QUEST_PHRASES.find((p) => p.id === id);
}

/** Get the total number of phrases in a stage */
export function getStageCount(stage?: number): number {
  if (stage === undefined) return QUEST_PHRASES.length;
  return QUEST_PHRASES.filter((p) => p.stage === stage).length;
}
